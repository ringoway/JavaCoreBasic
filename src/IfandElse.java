public class IfandElse {
    public static void main(String[] args) {
        int X=9;

        if (X>=30) { // if - если, else - то
            System.out.println("Число переменной X больше или равно 30");
        } else if (X>=20) {
            System.out.println("Число переменной X больше или равно 20"); }
            else if (X>=10) {
                System.out.println("Число переменной X больше или равно 10"); }
                else {
            System.out.println("Число переменной X меньше 10");

        }
    }
}
